/**
 * Book My Stay App
 *
 * This class represents the entry point of the Hotel Booking Management System.
 * It demonstrates how a Java application begins execution and prints a welcome
 * message along with application name and version.
 *
 * @author Vishesh
 * @version 1.0
 */

public class UC1HotelBookingApp {

    /**
     * The main method is the entry point of the Java application.
     * The JVM starts execution from this method.
     *
     * @param args command line arguments
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("========================================");
        System.out.println("        Welcome to Book My Stay");
        System.out.println("      Hotel Booking Management System");
        System.out.println("              Version 1.0");
        System.out.println("========================================");

        // Additional information
        System.out.println("Application started successfully.");
        System.out.println("Enjoy booking your hotel stay!");
        System.out.println("========================================");
    }
}